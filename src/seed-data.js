function normalize(map, raw) {
  if (!raw) return "";
  const k = String(raw).trim();
  return map[k] || map[k.toLowerCase()] || map[k.toUpperCase()] || k;
}

export function normProduct(raw) { return normalize(PRODUCT_NORMALIZE, raw); }
export function normColor(raw) { return normalize(COLOR_NORMALIZE, raw); }


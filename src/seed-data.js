// ============================================================================
//  匯出組合 SEED_TRANSACTIONS (供 App 初始化)
// ============================================================================
export function buildSeedTransactions(sanitizeFn) {
  const out = [];
  let idx = 0;
  for (const row of SEED_SHIPPING_TH) {
    const [date, client, productRaw, colorRaw, qtyRaw, price] = row;
    out.push({
      id: `seed-th-${++idx}`,
      date: date.replace(/\//g, "-"),
      type: "OUT",
      itemName: normProduct(productRaw),
      color: normColor(colorRaw),
      quantity: sanitizeFn(qtyRaw),
      rawQuantity: qtyRaw,
      client, origin: "ER",
      unitPrice: parseFloat(price) || 0,
      isVoided: false,
      createdAt: new Date().toISOString(),
    });
  }
  for (const row of SEED_SHIPPING_CN) {
    const [date, client, productRaw, colorRaw, qtyRaw, price] = row;
    out.push({
      id: `seed-cn-${++idx}`,
      date: date.replace(/\//g, "-"),
      type: "OUT",
      itemName: normProduct(productRaw),
      color: normColor(colorRaw),
      quantity: sanitizeFn(qtyRaw),
      rawQuantity: qtyRaw,
      client, origin: "CN",
      unitPrice: parseFloat(price) || 0,
      isVoided: false,
      createdAt: new Date().toISOString(),
    });
  }
  return out;
}
